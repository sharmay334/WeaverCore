package com.stpl.pms.commonJavabeans;

public class FrightReportBean {

	private String transportFright;
	private String loadFright;
	private String unloadFright;
	private String date;
	private String billVoucherNumber;
	private String localFright;

	public String getTransportFright() {
		return transportFright;
	}

	public void setTransportFright(String transportFright) {
		this.transportFright = transportFright;
	}

	public String getLoadFright() {
		return loadFright;
	}

	public void setLoadFright(String loadFright) {
		this.loadFright = loadFright;
	}

	public String getUnloadFright() {
		return unloadFright;
	}

	public void setUnloadFright(String unloadFright) {
		this.unloadFright = unloadFright;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getBillVoucherNumber() {
		return billVoucherNumber;
	}

	public void setBillVoucherNumber(String billVoucherNumber) {
		this.billVoucherNumber = billVoucherNumber;
	}

	public String getLocalFright() {
		return localFright;
	}

	public void setLocalFright(String localFright) {
		this.localFright = localFright;
	}

}
