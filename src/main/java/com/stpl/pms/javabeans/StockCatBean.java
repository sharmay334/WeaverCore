package com.stpl.pms.javabeans;

public class StockCatBean {

	private int stCtId;
	private String stockName;
	private String stock_under;

	public int getStCtId() {
		return stCtId;
	}

	public void setStCtId(int stCtId) {
		this.stCtId = stCtId;
	}

	public String getStockName() {
		return stockName;
	}

	public void setStockName(String stockName) {
		this.stockName = stockName;
	}

	public String getStock_under() {
		return stock_under;
	}

	public void setStock_under(String stock_under) {
		this.stock_under = stock_under;
	}

}
